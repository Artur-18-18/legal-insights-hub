const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem("lang");
    return (saved === "uz" ? "uz" : "ru") as Lang;
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("lang", l);
  };

  const t = (key: string) => {
    const val = translations[lang][key];
    if (!val) return key;
    return val.replace("{year}", new Date().getFullYear().toString());
  };

  useEffect(() => {
    document.documentElement.lang = lang === "uz" ? "uz" : "ru";
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

// Helper to resolve a localized field from an object with `field` and `field_uz` properties
export function useLocalized() {
  const { lang } = useI18n();
  const isUz = lang === "uz";

  return <T extends string>(obj: Record<string, T | undefined | null>, field: string): T | undefined | null => {
    if (isUz) {
      const uzField = `${field}_uz`;
      return (obj[uzField] as T | undefined) || obj[field] || null;
    }
    return obj[field] || null;
  };
}
