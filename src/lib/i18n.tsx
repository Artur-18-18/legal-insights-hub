const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem("lang");
    if (saved === "uz" || saved === "en" || saved === "ru") return saved as Lang;
    return "ru";
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("lang", l);
  };

  const t = (key: string, params?: Record<string, string | number>) => {
    const val = translations[lang][key];
    if (!val) return key;
    
    let result = val.replace("{year}", new Date().getFullYear().toString());
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        result = result.replace(`{${k}}`, v.toString());
      });
    }
    return result;
  };

  useEffect(() => {
    document.documentElement.lang = lang === "uz" ? "uz" : lang === "en" ? "en" : "ru";
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

/** Locale for date-fns format() */
export function getDateFnsLocale(lang: Lang) {
  if (lang === "uz") return uz;
  if (lang === "en") return enUS;
  return ru;
}

// Helper: ru → field; uz → field_uz || field; en → field_en || field || field_uz
export function useLocalized() {
  const { lang } = useI18n();

  return (obj: object | null | undefined, field: string): string | null => {
    if (!obj || typeof obj !== "object") return null;
    const o = obj as Record<string, unknown>;
    const asStr = (v: unknown): string | null => (typeof v === "string" ? v : null);
    if (lang === "uz") {
      return asStr(o[`${field}_uz`]) || asStr(o[field]) || null;
    }
    if (lang === "en") {
      return asStr(o[`${field}_en`]) || asStr(o[field]) || asStr(o[`${field}_uz`]) || null;
    }
    return asStr(o[field]) || null;
  };
}
